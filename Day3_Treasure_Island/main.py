print("Welcome to Treasure Island")
print("Your mission is to find the treasure")

direction = input("left or right?")

if direction.casefold() =="left".casefold():
    swim_or_wait = input("Swim or wait")
    if swim_or_wait.casefold() =="wait".casefold():
        door_color = input("Which door ?")
        if door_color.casefold() == "red".casefold():
            print("Burned by fire")
            print("Game over")
        elif door_color.casefold() =="blue".casefold():
            print("Eaten by beasts")
            print("Game over")
        elif door_color.casefold() =="yellow".casefold():
            print("You Win!")
        else:
            print("Game Over.")

    else:
        print("Attacked by trout")
        print("Game over")
else:
    print("Fall into a hole")
    print("Game over")
